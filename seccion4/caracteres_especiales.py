# Programa: Ejemplo de caracteres especiales en Python

# Salto de linea \n
mensaje1= "Hola\nMundo"
print(mensaje1)

#Tabulacion \t
# Imprimimos una guia numerica para ver las posiciones
print("123412341234123412341234")
mensaje2 = "\t\t Alex Yoza"
print(mensaje2)

"Comillas dentro de cadena"
mensaje3 = "Ella dijo: \"Hola a todos\""
print(mensaje3)

# Barra invertida \
mensaje4 = "C:\\Usuario\\Alex\\Documentos"
print(mensaje4)

# Cadena cruda (raw string) r
mensaje5 = r"C:\Usuario\Alex\Documentos"
print(mensaje5)