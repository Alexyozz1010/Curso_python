# Operadores Aritmetico

a = 10
b = 3

#suma +
suma = a + b
print(f"suma: {suma}")

#Resta -
resta = a - b
print(f"resta: {resta}")

#Multiplicacion *
multiplicacion = a * b
print(f"multiplicacion: {multiplicacion}")

#Division / (retorna float)
division = a / b
print(f"division: {division:.2f}")

#Division entera //
division_entera = a // b
print(f"division entera: {division_entera}")

#Modulo o Residuo %
residuo = a % b
print(f"residuo: {residuo}")

# Exponente o potencia **
exponente = a ** b #es 10^3 ya que a= 10 y b=3 "10*10*10" 
print(f"exponente: {exponente}")