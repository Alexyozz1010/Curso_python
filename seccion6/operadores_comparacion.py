print("*** Operadores Comparacion (Relacionales) ***")
a, b = 5, 5
print(f"Vallr inicial a: {a}, b: {b}")

#Operador igual ==
resultado = a == b
print(f"Tesultado de a == b es: {resultado} ")

#Resultado diferente !=
resultado = a != b
print(f"Tesultado de a != b es: {resultado} ")

#Operador mayor que >
resultado = a > b
print(f"Tesultado de a > b es: {resultado} ")

#Operador mayor que >=
resultado = a >= b
print(f"Tesultado de a >= b es: {resultado} ")

#Operador mayor que <
resultado = a < b
print(f"Tesultado de a < b es: {resultado} ")

#Operador mayor que <=
resultado = a <= b
print(f"Tesultado de a <= b es: {resultado} ")
